def _build_local_search_pages() -> list[dict]:
    if not ENABLE_GENERATED_LOCAL_SEARCH:
        return MANUAL_LOCAL_SEARCH_PAGES

    existing_slugs = {page["slug"] for page in MANUAL_LOCAL_SEARCH_PAGES}
    generated = _build_council_wave_1_pages(existing_slugs)
    generated.extend(_build_county_wave_1_pages(existing_slugs))
    return MANUAL_LOCAL_SEARCH_PAGES + generated



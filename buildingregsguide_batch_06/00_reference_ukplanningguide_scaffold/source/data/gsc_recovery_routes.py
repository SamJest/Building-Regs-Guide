def recovery_slug(route: dict) -> str:
    return "-".join(route["scenario_slugs"])


def recovery_path(route: dict) -> str:
    return (
        f"/{route['project_slug']}/{route['county_slug']}/"
        f"{route['town_slug']}/{recovery_slug(route)}/"
    )


GSC_RECOVERY_PATHS = frozenset(recovery_path(route) for route in GSC_RECOVERY_ROUTES)


def is_gsc_recovery_path(path: str) -> bool:
    clean = "/" + str(path or "").strip("/")
    if clean != "/":
        clean += "/"
    return clean in GSC_RECOVERY_PATHS


def gsc_recovery_routes_for_project(project_slug: str, county_slug: str, town_slug: str) -> tuple[dict, ...]:
    return tuple(
        route
        for route in GSC_RECOVERY_ROUTES
        if route["project_slug"] == project_slug
        and route["county_slug"] == county_slug
        and route["town_slug"] == town_slug
    )
